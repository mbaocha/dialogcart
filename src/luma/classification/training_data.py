def validate_examples(examples):
    errors = []
    for text, labels in examples.items():
        tokens = text.split()
        if len(tokens) != len(labels):
            errors.append({
                "text": text,
                "tokens": tokens,
                "n_tokens": len(tokens),
                "labels": labels,
                "n_labels": len(labels),
                "diff": len(tokens) - len(labels)
            })
    return errors

